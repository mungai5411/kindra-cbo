/**
 * Get default center coordinates for a county
 */
export const getCountyCenter = (countyName: string): [number, number] => {
    const coords = COUNTY_COORDINATES[countyName];
    return coords ? [coords.lat, coords.lng] : [-1.286389, 36.817223]; // Default to Nairobi if not found
};

/**
 * Get sub-counties for a given county
 */
export const getSubCounties = (countyName: string): string[] => {
    const county = KENYA_COUNTIES.find(c => c.name === countyName);
    return county ? county.sub_counties : [];
};

/**
 * Search counties by name (supports partial matching)
 */
export const searchCounties = (query: string): typeof KENYA_COUNTIES => {
    const lowerQuery = query.toLowerCase().trim();
    return KENYA_COUNTIES.filter(county =>
        county.name.toLowerCase().includes(lowerQuery)
    );
};
